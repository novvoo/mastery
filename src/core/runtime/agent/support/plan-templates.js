function cloneTaskWithCanonicalIds(task, idMap) {
  const mappedId = idMap[task.id] || task.id;
  const mappedDependencies = (task.dependencies || [])
    .map((dependency) => idMap[dependency] || dependency)
    .filter(
      (dependency, index, dependencies) =>
        dependency !== mappedId && dependencies.indexOf(dependency) === index,
    );
  const allowedTools =
    mappedId === 'inspect_workspace'
      ? Array.from(
          new Set([
            ...(task.allowedTools || []),
            'list_dir',
            'read_file',
            'glob',
            'search',
            'search_codebase',
            'semantic_search',
            'grep_search',
            'file_search',
            'tree',
            'stat_file',
            'shell',
            'project_profile',
          ]),
        )
      : task.allowedTools;

  return {
    ...task,
    id: mappedId,
    dependencies: mappedDependencies,
    allowedTools,
  };
}

function withCanonicalTaskIds(template, type) {
  const idMap = CANONICAL_TASK_ID_MAPS[type];
  if (!idMap) {
    return template;
  }

  return {
    ...template,
    tasks: template.tasks.map((task) => cloneTaskWithCanonicalIds(task, idMap)),
  };
}

