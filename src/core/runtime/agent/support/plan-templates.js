export function getPlanTemplateByTaskType(taskType) {
  const typeMap = {
    research: PLAN_TEMPLATES.RESEARCH,
    analysis: PLAN_TEMPLATES.ANALYSIS,
    verification: PLAN_TEMPLATES.VERIFICATION,
    quick: PLAN_TEMPLATES.QUICK,
    standard: PLAN_TEMPLATES.STANDARD,
    bug_fix: PLAN_TEMPLATES.BUG_FIX,
    documentation: PLAN_TEMPLATES.DOCUMENTATION,
    refactor: PLAN_TEMPLATES.REFACTOR,
    testing: PLAN_TEMPLATES.TESTING,
    code_review: PLAN_TEMPLATES.CODE_REVIEW,
    migration: PLAN_TEMPLATES.MIGRATION,
    setup: PLAN_TEMPLATES.SETUP,
    release: PLAN_TEMPLATES.RELEASE,
    security: PLAN_TEMPLATES.SECURITY,
    data: PLAN_TEMPLATES.DATA,
    ui: PLAN_TEMPLATES.UI,
    new_project: PLAN_TEMPLATES.NEW_PROJECT,
    coding: PLAN_TEMPLATES.STANDARD,
    modification: PLAN_TEMPLATES.STANDARD,
    general: PLAN_TEMPLATES.RESEARCH,
  };
  const normalizedType = taskType?.toLowerCase();
  if (normalizedType === 'quick') {
    return createQuickTemplate();
  }
  if (normalizedType === 'new_project') {
    return createNewProjectTemplate();
  }
  const template = typeMap[normalizedType] || PLAN_TEMPLATES.STANDARD;
  const canonicalType =
    normalizedType === 'coding' || normalizedType === 'modification' || !normalizedType
      ? 'standard'
      : normalizedType;
  return withCanonicalTaskIds(template, canonicalType);
}

